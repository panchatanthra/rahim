package hellopack;

public class SpecialAssignmentOperatorsDemo {
	public static void main(String[] args) {
		int n=10;
		
		n+=5;//n=n+5;
		System.out.println(n);//15

		n=10;
		n-=3;//n=n-3;
		System.out.println(n);//7
		
		n=10;
		n*=5;//n=n*5;
		System.out.println(n);//50

		n=10;
		n/=5;//n=n/5;
		System.out.println(n);//2
		
		n=10;
		n%=3;//n=n%3;
		System.out.println(n);//1
		
	}
}
